-- Supabase SQL setup for the static dubbing vote app.
-- Run this in Supabase Dashboard -> SQL Editor.

create table if not exists groups (
  id int primary key,
  name text default '',
  work_title text default '',
  members text default '',
  has_performed boolean default false,
  created_at timestamptz default now()
);

create table if not exists event_state (
  id int primary key,
  current_group_id int references groups(id),
  phase text default 'idle',
  voting_open boolean default false,
  voting_start_time timestamptz,
  canvassing_end_time timestamptz,
  voting_end_time timestamptz,
  show_ranking boolean default false,
  updated_at timestamptz default now()
);

create table if not exists votes (
  id bigint generated by default as identity primary key,
  group_id int not null references groups(id),
  voter_token text not null,
  score int not null check (score >= 1 and score <= 10),
  created_at timestamptz default now(),
  unique (group_id, voter_token)
);


