-- Simple public policies for static hosting.
-- Important: This is convenient for a school event, not secure against technical students.
-- For stricter security, use a backend or Supabase Edge Functions for admin actions.

alter table groups enable row level security;
alter table event_state enable row level security;
alter table votes enable row level security;

drop policy if exists "public read groups" on groups;
drop policy if exists "public modify groups" on groups;
drop policy if exists "public read event_state" on event_state;
drop policy if exists "public modify event_state" on event_state;
drop policy if exists "public read votes" on votes;
drop policy if exists "public insert votes" on votes;
drop policy if exists "public delete votes" on votes;

create policy "public read groups" on groups for select using (true);
create policy "public modify groups" on groups for update using (true) with check (true);

create policy "public read event_state" on event_state for select using (true);
create policy "public modify event_state" on event_state for update using (true) with check (true);

create policy "public read votes" on votes for select using (true);
create policy "public insert votes" on votes for insert with check (true);
create policy "public delete votes" on votes for delete using (true);
